#include "libft.h"

int		ft_atoi(const char *str)
{
	int result;
	int sign;

	result = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (ft_isdigit(*str))
	{
		result *= 10;
		result += *str - '0';
		str++;
	}
	if (sign == -1)
		return (-result);
	return (result);
}
